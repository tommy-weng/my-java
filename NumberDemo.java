public class DemoNumber {
    public static void main (String[] args) {
        double num = 1.4; 
        System.out.println("floor(" + num + ") = " + Math.floor(num));
        System.out.println("round(" + num + ") = " + Math.round(num));
        System.out.println("ceil(" + num + ") = " + Math.ceil(num));
    }
}
